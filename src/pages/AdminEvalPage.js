function getGrade(score) {
  if (score >= 90) return "üåû ÌÉúÏñë";
  if (score >= 80) return "ü™ê ÌÜ†ÏÑ±";
  if (score >= 70) return "üåè ÏßÄÍµ¨";
  if (score >= 60) return "üçâ ÏàòÎ∞ï";
  if (score >= 50) return "üçà Î©úÎ°†";
  if (score >= 40) return "üçé ÏÇ¨Í≥º";
  if (score >= 30) return "ü•ù ÌÇ§ÏúÑ";
  return "ü´ê Î∏îÎ£®Î≤†Î¶¨";
}

